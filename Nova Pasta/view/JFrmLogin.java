    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        query1 = entityManager1.createQuery("select u from Usuario u where u.usuario = :usuario and u.senha = :senha");
        
        query1.setParameter("usuario", txt_login.getText());
        query1.setParameter("senha", txt_senha.getText());
        list1.clear();
        list1.addAll(query1.getResultList());
        
        
        if(list1.size() == 1){
            JFrmPrincipal frm = new JFrmPrincipal();
            frm.setVisible(true);
            setVisible(false);
        }else{
            JOptionPane.showMessageDialog(null, "Usu√°rio ou senha invalidos", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

